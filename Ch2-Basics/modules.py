# import the math module, which contains features for working with mathematics
import math

# the math module contains lots of pre-built functions
print("The square root of 16 is", math.sqrt(16))   # The square root of 16 is 4.0

# in addition to functions, some modules contain useful constants
print("Pi is", math.pi)   # 3.141592653589793

# try some of the math functions
print(math.floor(4.333))  # 4
print(math.ceil(8.8888))  # 9
